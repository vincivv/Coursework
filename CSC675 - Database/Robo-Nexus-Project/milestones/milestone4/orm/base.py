    @classmethod
    def all(cls):
        """Retrieve all records of this model from the database.
        """
        # SELECT * FROM <table>
        table = cls.table_descriptor()

        query = f"SELECT * FROM {table}"

        connection, cursor = MySQL.instance()

        try:
            cursor.execute(query)
            results = cursor.fetchall()
            all_results = []
            for result in results:
                all_results.append(cls(**result))
            return all_results

        except Exception as e:
            # handle errors and any rollbacks
            raise e

        finally:
            # close components to avoid memory leaks
            connection.close()
            cursor.close()



    @classmethod
    def query(cls, **filters):
        """Query records based on filters.

        TODO:
            - Open a connection and cursor.
            - Construct the `SELECT` SQL query using the provided filters as conditions.
            - Ensure the connection and cursor are properly closed after the operation.
            - Return the results as instances of the model.
        """
        table = cls.table_descriptor()
        where_clause = " AND ".join([f"{field} = %s" for field in filters.keys()])
        query = f"SELECT * FROM {table}"
        values = ()
        if filters:
            query += f" WHERE {where_clause}"
            values = tuple(filters.values())

        connection, cursor = MySQL.instance()
        try:
            cursor.execute(query, values)
            results = cursor.fetchall()
            return [cls(**result) for result in results]
        finally:
            connection.close()
            cursor.close()

    @classmethod
    def filter(cls, **filters):
        return cls.query(**filters)

    @classmethod
    def create(cls, **attributes):
        """Insert a new record in an existing schema without exposing the class constructor.
        """
        object = cls(**attributes)
        object._insert()
        return object

    @classmethod
    def create_schema(cls):
        """Generate the schema for the model in the database.

        TODO:
            - Open a connection and cursor.
            - Construct the `CREATE SCHEMA` SQL query using the provided descriptor.
            - Ensure the connection and cursor are properly closed after the operation.
            - Commit the transaction if successful; rollback if there's an error.
        """
        table = cls.table_descriptor()
        columns = cls.columns()
        if not columns:
            raise ValueError(f"{cls.__name__} does not define any columns.")

        column_definitions = []
        for name, column in columns.items():
            column_definitions.append(f"{name} {column.to_sql()}")

        schema = ", ".join(column_definitions)
        query = f"CREATE TABLE IF NOT EXISTS {table} ({schema})"

        connection, cursor = MySQL.instance()

        try:
            cursor.execute(query)
            connection.commit()
            return query
        except Exception as e:
            connection.rollback()
            raise e
        finally:
            cursor.close()
            connection.close()

    @classmethod
    def join(cls, models):
        """Join multiple models to organize your data.

        TODO:
            - Open a connection and cursor.
            - Construct the appropriate `JOIN` SQL query to combine data from multiple models.
            - Ensure the connection and cursor are properly closed after the operation.
            - Return the joined results.
        """
        if not isinstance(models, (list, tuple)):
            models = [models]
        if not models:
            raise ValueError("At least one model must be provided for join().")

        select_parts = []
        joined_tables = [cls]
        from_table = cls.table_descriptor()

        for model in [cls, *models]:
            table_name = model.table_descriptor()
            for column_name in model.columns().keys():
                alias = f"{table_name}__{column_name}"
                select_parts.append(f"{table_name}.{column_name} AS {alias}")

        join_clauses = []
        for model in models:
            match = None
            for joined_model in joined_tables:
                left_table = joined_model.table_descriptor()
                right_table = model.table_descriptor()

                for column_name, column in joined_model.columns().items():
                    if not isinstance(column.foreign_key, str):
                        continue

                    target_table, target_column = column.foreign_key.strip().rstrip(")").split("(", 1)
                    if target_table == right_table:
                        match = f"{left_table}.{column_name} = {right_table}.{target_column}"
                        break

                if match is not None:
                    break

                for column_name, column in model.columns().items():
                    if not isinstance(column.foreign_key, str):
                        continue

                    target_table, target_column = column.foreign_key.strip().rstrip(")").split("(", 1)
                    if target_table == left_table:
                        match = f"{right_table}.{column_name} = {left_table}.{target_column}"
                        break

                if match is not None:
                    break

            if match is None:
                raise ValueError(
                    f"No foreign-key relationship found between joined models "
                    f"and {model.__name__}."
                )

            join_clauses.append(f"JOIN {model.table_descriptor()} ON {match}")
            joined_tables.append(model)

        query = (
            f"SELECT {', '.join(select_parts)} "
            f"FROM {from_table} "
            f"{' '.join(join_clauses)}"
        )

        connection, cursor = MySQL.instance()
        try:
            cursor.execute(query)
            return cursor.fetchall()
        finally:
            cursor.close()
            connection.close()

    @classmethod
    def where(cls, **conditions):
        """Build a WHERE clause dynamically."""
        if not conditions:
            return "", ()

        clause = " WHERE " + " AND ".join(
            [f"{column} = %s" for column in conditions.keys()]
        )
        values = tuple(conditions.values())

        return clause, values

    @classmethod
    def having(cls, **conditions):
        """Build a HAVING clause dynamically."""
        if not conditions:
            return "", ()

        clause = " HAVING " + " AND ".join(
            [f"{column} = %s" for column in conditions.keys()]
        )
        values = tuple(conditions.values())

        return clause, values

    @classmethod
    def group_by(cls, *columns):
        """Build a GROUP BY clause dynamically."""
        if not columns:
            return ""

        return " GROUP BY " + ", ".join(columns)

    @classmethod
    def table_descriptor(cls):
        """
        :return: the name of the table t
        """
        return getattr(cls, "__tablename__", cls.__name__)


    @classmethod
    def columns(cls):
        """
        :return: the list of column names
        """
        return {name:column for name, column in cls.__dict__.items() if isinstance(column, Column)}



    @classmethod
    def primary_key(cls):
        """
        :return: the primary key attribute name, not the value.
                 for example student_id = 1, will return student_id, not the value 1
        """
        columns = cls.columns()
        for name, column in columns.items():
            if column.is_primary_key():
                return name
        return 'id'



    @classmethod
    def resolve_model(cls, model_name):
        return cls._registry[model_name]
    
    @classmethod
    def identity_key(cls, pk_value):
         return (cls.table_descriptor(), pk_value)

    @classmethod
    def get_identity_instance(cls, pk_value):
        key = cls.identity_key(pk_value)
        return cls._identity_map.get(key)

    @classmethod
    def add_identity_instance(cls, obj):
        pk = cls.primary_key()
        pk_value = getattr(obj, pk, None)
        if pk_value is None:
            return obj
        key = cls.identity_key(pk_value)
        cls._identity_map[key] = obj
        return obj
