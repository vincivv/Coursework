# base.py
#
# This file defines the `Base` class, which serves as the foundation for all ORM models.
# The `Base` class provides essential methods for interacting with the database, such as:
#   - `save()`: Insert or update the current model instance in the database.
#   - `_insert()`: Insert the current instance into the database (private method).
#   - `_update()`: Update the current instance in the database (private method).
#   - `get()`: Retrieve a record by its ID.
#   - `delete()`: Delete a record by its ID.
#   - `all()`: Retrieve all records of the model from the database.
#   - `query()`: Query records based on filter conditions.
#   - `create_schema()`: Generate the schema (sql code) for the model in the database.
#   - `create()`: Uses the generated schema from the previous function to create a table in the database based on the model's schema.
#   - `join()`: Join multiple models together for data retrieval.
#   - `where()`: Add WHERE conditions to queries.
#   - `having()`: Add HAVING conditions to queries.
#   - `group_by()`: Add GROUP BY clauses to queries.
#
# Connection management is critical. Every method interacting with the database must:
#   - Open a new connection and cursor at the start of the operation.
#   - Close the cursor and connection after the operation is complete,
#     whether the operation is successful or not, to avoid memory leaks.
#   - Transactions must be committed on success, and rolled back on failure.
#
# Students should implement proper connection management in each method, including:
#   - Using `try`, `except`, and `finally` to ensure the connection and cursor are always closed.
#   - Handling potential exceptions during database operations and performing rollbacks if needed.
#
# The `Base` class is meant to be subclassed, and any model that extends `Base` will automatically
# inherit the methods for database interaction.


from orm.db_connectors import MySQL
from orm.columns import Column


class Base:
    _registry = {}
    _identity_map = {}

    def __init_subclass__(cls, **kwargs):
        super().__init_subclass__(**kwargs)
        if cls is Base:
            return
        Base._registry[cls.table_descriptor()] = cls

    def __init__(self, **kwargs):
        """Initialize model instance with attributes."""
        for key, value in kwargs.items():
            setattr(self, key, value)


    def save(self):
        """Insert or update the record in the database.
        """
        pk = self.primary_key()

        if pk in self.__dict__ and self.__dict__[pk] is not None:
            print("I am updating...")
            self._update()
        else:
            print("I am inserting")
            self._insert()


    def _insert(self):
        """Insert the current instance into the database.
        """
        # INSERT INTO <table> (attributes separated by comma) VALUES
        #                      (placeholders separated by comma)
        table = self.table_descriptor()
        columns = self.columns()
        attributes = [attr for attr, column in columns.items() if not column.is_primary_key()]
        fields = ", ".join(attributes)
        values = [self.__dict__[attr] for attr in attributes]
        placeholders = ", ".join(["%s"] * len(attributes))

        
        query = f"INSERT INTO {table} ({fields}) VALUES ({placeholders})"

        connection, cursor = MySQL.instance()

        try:
            cursor.execute(query, values)
            connection.commit()
            pk_value = cursor.lastrowid
            if pk_value:
                pk = self.primary_key() # artist_id
                setattr(self, pk, pk_value) # new id generated by the insert and assigning it to that pk
                self.__class__.add_identity_instance(self)

        except Exception as e :
            # handle errors and any rollbacks
            connection.rollback()
            raise e

        finally:
            # close components to avoid memory leaks
            connection.close()
            cursor.close()






    def _update(self):
        """Update the current instance in the database.
        """
        # UPDATE <table> SET <attr1=placehoder1>, <attr2=placeholder2>, ...
        #                    <attrn=placeholdern> WHERE <pk> = <placehoder>

        table = self.table_descriptor()
        columns = self.columns()
        attributes = [attr for attr, column in columns.items() if not column.is_primary_key()]
        values = [self.__dict__[attr] for attr in attributes]
        pk = self.primary_key()
        pk_value = getattr(self, pk, None)
        values.append(pk_value)
        fields = [f"{attr}=%s" for attr in attributes]
        fields_separated_by_comma = ", ".join(fields)

        query = f"UPDATE {table} SET {fields_separated_by_comma} WHERE {pk} = %s"
        print(query)
        connection, cursor = MySQL.instance()

        try:
            cursor.execute(query, values)
            connection.commit()
        except Exception as e:
            # handle errors and any rollbacks
            connection.rollback()
            raise e

        finally:
            # close components to avoid memory leaks
            connection.close()
            cursor.close()


    @classmethod
    def get(cls, **filters):
        """Retrieve a record from the database by its ID.
        """
        # SELECT * FROM <table> WHERE <field> = %s AND ...
        table = cls.table_descriptor()
        if not filters:
            raise ValueError("At least one filter is required.")
        where_clause = " AND ".join([f"{field} = %s" for field in filters.keys()])
        query = f"SELECT * FROM {table} WHERE {where_clause}"
        values = tuple(filters.values())

        connection, cursor = MySQL.instance()

        try:
            cursor.execute(query, values)
            result = cursor.fetchone()
            if result is None:
                return None
            pk_name = cls.primary_key()
            pk_value = result.get(pk_name) if isinstance(result, dict) else result[pk_name]
            existing = cls.get_identity_instance(pk_value)
            if existing is not None:
                return existing
            obj = cls(**result)
            cls.add_identity_instance(obj)
            return obj

        except Exception as e :
            # handle errors and any rollbacks
            raise e

        finally:
            # close components to avoid memory leaks
            connection.close()
            cursor.close()

